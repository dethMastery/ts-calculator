export default class calcView {
  add(no1: number, no2: number): any {
    return no1 + no2
  }

  subt(no1: number, no2: number): any {
    return no1 - no2
  }

  multiply(no1: number, no2: number): any {
    return no1 * no2
  }

  divide(no1: number, no2: number): any {
    return no1 / no2
  }
}